# Bitwise (|)
result_a = 12 | 25
print(f"12 | 25 = {result_a}")

# Bitwise (&)
result_b = 21 & 27
print(f"21 & 27 = {result_b}")

# Bitwise (^)
result_c = 8 ^ 7
print(f" 8 ^ 7 = {result_c}")

# Bitwise (&)
result_d = 13 & 7
print(f" 13 & 7 = {result_d}")

# Bitwise (|)
result_e = 13 | 7
print(f" 13 | 7 = {result_e}")
